    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
        // mengedit data tabel yang diselect
        Database database = new Database();
        int row;
        String input, id, column;
        if(getColumn() != 0){
            row = inventoryTable.getSelectedRow();
            id = table.getValueAt(row, 0).toString();
            input = inputTxt.getText();
            column = inputCobox.getItemAt(getColumn());
            database.updateInventory(id, input, column);
            table.setValueAt(input, row, getColumn() - 1);
        } else {
            JOptionPane.showMessageDialog(null, "Choose Column in ComboBox", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editBtnMouseClicked

    private void deleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnMouseClicked
        // menghapus data jika data tabel yang diselect berjumlah satu
        int[] selectedRows = inventoryTable.getSelectedRows();        
        Database databases = new Database();
        if(selectedRows.length == 1) {
            int row = inventoryTable.getSelectedRow();
            String id = table.getValueAt(row, 0).toString();
            table.removeRow(inventoryTable.getSelectedRow());
            databases.deleteInventory(id);
        } else if(selectedRows.length == 0) { 
            JOptionPane.showMessageDialog(null, "Harap Pilih Data Terlebih Dahulu");
        } else {
            JOptionPane.showMessageDialog(null, "Harap Delete Satu per Satu");
        }
    }//GEN-LAST:event_deleteBtnMouseClicked

