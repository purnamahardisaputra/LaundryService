    // mensetting tabel yang ada dan menerima datanya dari kelas Storage
    public void setTable(Storage storage) {
        ArrayList<ArrayList<String>> dataString = storage.showDataString();
        String[] cobox = {"Pilih Kolom", "ID", "Customer", "Service", "Day", "Laundry", "Payment", "Items", "Price"};
        String[] column = {"ID", "Customer", "Service", "Day", "Laundry", "Payment", "Items", "Price"};
        
        table.setColumnCount(0);
        table.setRowCount(0);
        for(int i = 0; i < column.length; i++){
            table.addColumn(column[i]);
        }
        
        for(int i = 0; i < cobox.length; i++){
            searchCobox.addItem(cobox[i]);
            inputCobox.addItem(cobox[i]);
        }
        
        // Ambil data string
        int numColumns = dataString.size();
        int numRows = dataString.get(0).size(); // Anggap semua ArrayList memiliki ukuran yang sama
        for (int i = 0; i < numRows; i++) {
            Object[] row = new Object[numColumns + 2]; // Jumlah kolom tambahan untuk items dan price
            for (int j = 0; j < numColumns; j++) {
                row[j] = dataString.get(j).get(i);
            }
            // Memasukkan items dan price ke kolom terakhir
            row[numColumns] = storage.showDataDouble().get(0).get(i); // Items
            row[numColumns + 1] = storage.showDataDouble().get(1).get(i); // Price
            table.addRow(row);
        }
        
        inventoryTable.setModel(table);
    }
    
    // mengembalikan nilai combo box yang dipilih dalam bentuk index
    public int getIndex() {
        return searchCobox.getSelectedIndex() - 1;
    }
    
    // mengembalikan nilai combo box yang dipilih dalam bentuk index
    public int getColumn() {
        return inputCobox.getSelectedIndex();
    }
    
    // mencari data berdasarkan combo box yang dipilih
    private void filter(String query) {
        table = (DefaultTableModel) inventoryTable.getModel();
        TableRowSorter<DefaultTableModel> tr = new 
        TableRowSorter<DefaultTableModel> (table);
        
        inventoryTable.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(query, getIndex()));
    }

