    private void updateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateBtnMouseClicked
        // mengupdate nilai status pada tabel
        Database database = new Database();
        int row;
        String status, id;
        if(getStatus() != 0){
            row = trackingTable.getSelectedRow();
            id = table.getValueAt(row, 0).toString();
            status = statusCobox.getItemAt(getStatus());
            database.updateStatus(id, status);
            table.setValueAt(status, row, 6);
        } else {
            JOptionPane.showMessageDialog(null, "Choose Input in ComboBox", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateBtnMouseClicked

