    //mensetting tabel yang ada dan menerima datanya dari kelas Tracking
    public void setTable(Tracking tracking) {
        ArrayList<ArrayList<String>> dataString = tracking.showDataString();
        String[] cobox = {"Pilih Status", "Pending", "On Progress", "Success"};
        String[] column = {"ID", "Customer", "Service", "Day", "Laundry", "Payment", "Status", "Items", "Price"};
        
        table.setColumnCount(0);
        table.setRowCount(0);
        for(int i = 0; i < column.length; i++){
            table.addColumn(column[i]);
        }
        
        for(int i = 0; i < cobox.length; i++){
            statusCobox.addItem(cobox[i]);
        }
        
        // Ambil data string
        int numColumns = dataString.size();
        int numRows = dataString.get(0).size(); // Anggap semua ArrayList memiliki ukuran yang sama

        for (int i = 0; i < numRows; i++) {
            Object[] row = new Object[numColumns + 2]; // Jumlah kolom tambahan untuk items dan price
            for (int j = 0; j < numColumns; j++) {
                row[j] = dataString.get(j).get(i);
            }
            // Memasukkan items dan price ke kolom terakhir
            row[numColumns] = tracking.showDataDouble().get(0).get(i); // Items
            row[numColumns + 1] = tracking.showDataDouble().get(1).get(i); // Price
            table.addRow(row);
        }
        trackingTable.setModel(table);
    }
    
    // mengembalikan nilai combo box yang dipilih dalam bentuk index
    public int getStatus() {
        return statusCobox.getSelectedIndex();
    }

