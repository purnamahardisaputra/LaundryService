    private void confirmBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmBtnMouseClicked
        // menanyakan apakah sudah bayar atau belum
        Invoice invoice = new Invoice(transaction);
        int jawab = JOptionPane.showOptionDialog(this, "Have you paid?", "Confirm", JOptionPane.YES_NO_OPTION, 
                JOptionPane.QUESTION_MESSAGE, null, null, null);
        if (jawab == JOptionPane.YES_OPTION){
            invoice.setVisible(true);
            this.dispose();
        } else if (jawab == JOptionPane.NO_OPTION){
            JOptionPane.showMessageDialog(null, "Confirm payment first", "Confirm Payment", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_confirmBtnMouseClicked

