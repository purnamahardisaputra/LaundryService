    // mengubah ukuran dari kode qr agar sesuai dengan labelnya
    public ImageIcon resizeImage(ImageIcon myImage){          
        Image img = myImage.getImage();
        Image img2 = img.getScaledInstance(qrisLabel.getHeight(), qrisLabel.getWidth(),  Image.SCALE_SMOOTH);
        ImageIcon image=new ImageIcon(img2);
        return image;
    }

